package Features;

public class Products {
    String code;
    String name;
    String brand;
    Double price;
    int quantity;
    String detail;
    String date;
    String picture;

    public Products(String code, String name, String brand, Double price, int quantity, String detail, String date, String picture){
        this.code = code;
        this.name = name;
        this.brand = brand;
        this.price = price;
        this.quantity = quantity;
        this.detail = detail;
        this.date = date;
        this.picture = picture;
    }



}
